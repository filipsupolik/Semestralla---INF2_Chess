package enums;
/**
 * The PlayerType enum represents the type of player in a game.
 * It can be one of the following:
 * <ul>
 *   <li>{@link #WHITE} - Represents a white player.</li>
 *   <li>{@link #BLACK} - Represents a black player.</li>
 *   <li>{@link #EMPTY} - Represents an empty or unoccupied state.</li>
 * </ul>
 */
public enum PlayerType {
    WHITE,
    BLACK,
    EMPTY;
}
